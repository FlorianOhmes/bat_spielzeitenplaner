package de.bathesis.spielzeitenplaner.database.entities;

import org.springframework.data.annotation.Id;
import java.time.LocalDate;


public record Assessment(@Id Integer id, LocalDate date, Integer criterionId, Integer playerId, Double value) {

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Assessment other = (Assessment) obj;
        if (id == null) {
            if (other.id != null)
                return false;
        } else if (!id.equals(other.id))
            return false;
        return true;
    }

}
